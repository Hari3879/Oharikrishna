package FlowControls;
/**
 * printing tables 1 to 10
 * @author ho22078
 *
 */

public class NestedForLoop {

	public static void main(String[] args) {
		for (int i=1;i<=10;++i) {
		for (int j=1;j<=10;++j) {
				System.out.println(i+ "*" +j+"=" +i*j);
			}
		System.out.println("     ");
				
		}
		}
			

	



}

